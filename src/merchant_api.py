    def _get_client(self):
        """Lazy load Merchant API client."""
        if self._client is None:
            try:
                # Use default credentials (GOOGLE_APPLICATION_CREDENTIALS)
                self._client = merchant_center_reports_v1beta.MerchantCenterReportsServiceClient()
                logger.info("✓ Merchant API client initialized")
            except Exception as e:
                logger.error(f"Failed to initialize Merchant API client: {e}")
                raise
